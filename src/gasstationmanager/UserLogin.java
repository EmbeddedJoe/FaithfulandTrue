    private void txtLoginUsernameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtLoginUsernameActionPerformed
        String username = txtLoginUsername.getText();
        if(!checkUsernameLogin(username)) {
            JOptionPane.showMessageDialog(txtLoginUsername, "Please enter a valid username", "Input Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_txtLoginUsernameActionPerformed

    private void pwdLoginPasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pwdLoginPasswordActionPerformed
        char[] password = pwdLoginPassword.getPassword();
        String strPassword = Arrays.toString(password);
        if(!checkPasswordLogin(password,strPassword)) {
            JOptionPane.showMessageDialog(pwdLoginPassword, "Please enter a password", "Input Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_pwdLoginPasswordActionPerformed

    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String LoginUsername = txtLoginUsername.getText();
        char[] LoginPassword = pwdLoginPassword.getPassword();
        
        UserLogin myLogin = new UserLogin();
        StaffRecordsConnector loginConnector = new StaffRecordsConnector();
        loginConnector.createConnection();
        PreparedStatement LoginPS = loginConnector.createStatement(myLogin);
        if(!loginConnector.authenticateUser(LoginPS)) {
         JOptionPane.showMessageDialog(pwdLoginPassword, "The username and password do not match", "Login Error", JOptionPane.ERROR_MESSAGE);
        }
        else {
        JOptionPane.showMessageDialog(pwdLoginPassword, "You have logged in Successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
        WelcomeUser loadWelcome = new WelcomeUser();
        loadWelcome.setVisible(true);
        loadWelcome.setSize(500, 500);
        }
    }//GEN-LAST:event_btnLoginActionPerformed

