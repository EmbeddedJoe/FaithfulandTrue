    private boolean checkUsernameLogin(String username) {
        boolean isValid = true;
        
        if(username.equalsIgnoreCase("")) {
            isValid = false;
        }
        return isValid;
    }
    
    private boolean checkPasswordLogin(char[] password, String strPassword) {
        boolean isValid = true;
        strPassword = Arrays.toString(password);
        if(strPassword.equalsIgnoreCase("")) {
            isValid = false;
        }
        return isValid;
    }
    
    public String LoginPassword() {
        char[] password = pwdLoginPassword.getPassword();
        String strPassword = Arrays.toString(password);
        return strPassword;
    }
    
    public String LoginUsername() {
        String username = txtLoginUsername.getText();
        return username;
    }
    
    
